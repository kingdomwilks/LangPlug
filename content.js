//Function to review DOM elements
function findAndReplace(spanishEdexcelGCSE){
  elementsInsideBody.forEach(element =>{
    element.childNodes.forEach(child =>{
      if(child.nodeType === 3){
        replaceText(child, spanishEdexcelGCSE);
        console.log('replaced');
      }
    });
  });
}

function replaceText (node, spanishEdexcelGCSE) {
  let value = node.nodeValue;

  let spanish = Object.keys(spanishEdexcelGCSE);
  let english = Object.values(spanishEdexcelGCSE);

  for (var i = 0; i < spanish.length; i++) {

      var spanishString = spanish[i]
      var englishString = english[i]
      var space = " "
      var spacedSpanishString = space.concat(spanishString, " ");
      var spacedEnglishString = space.concat(englishString, " ");
      
      //Regex using variables
      var regexFunction = new RegExp(spacedSpanishString, "gi")

      // New value = old value replaced
      value = value.replace(regexFunction, spacedEnglishString);

      //Return new value to the DOM
      node.nodeValue = value;
      //}
    //}
  }
} 


